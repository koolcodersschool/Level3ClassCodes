package day9;

public class MethodOverriding {
	
	/*
	 * Method Overriding:
	 * 1 - There should be inheritance
	 * 2 - Access modifier should be same or more visible
	 * 3 - method name should be same
	 * 4 - parameters/arguments should be same
	 * 5 - return type should be same
	 * 
	 * When method overriding will not happen:
	 * 1 - if the method is private we can't override that method
	 * 2 - if the method is final we can't override that method
	 * 3 - if the method is static we can't override that method
	 */

}
